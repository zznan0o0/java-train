    static public <T> Map<List<Object>, T> list2MapByKeys(List<T> list, List<Function<T, Object>> fns){
        Map<List<Object>, T> map = new HashMap<>();

        for(T v : list){
            List<Object> keys = new ArrayList<>();
            for(Function<T, Object> fn : fns){
                keys.add(fn.apply(v));
            }
            map.put(keys, v);
        }

        return map;
    }

